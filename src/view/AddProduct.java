    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        String name = name_txt.getText();
        String price = price_txt.getText();
        String stock = stock_txt.getText();
        String extra = extra_txt.getText();
        
        //if no product type is selected display an error
        if(!clothing_rdb.isSelected() && !footwear_rdb.isSelected()){
            error_lbl.setText("Error: Please select a product type");
        }
        //if any text boxes are empty display an error message
        else if(name.equals("") || price.equals("") || stock.equals("")){
            error_lbl.setText("Error: Please complete all fields");
        }
        else{
            try{
                double dblPrice = Double.parseDouble(price);
                int intStock = Integer.parseInt(stock);
                try{
                    if(footwear_rdb.isSelected()){
                            int intSize = Integer.parseInt(extra);
                            //int sizeIn, String productNameIn, int productIdIn, double priceIn, int stockLevelIn
                            Footwear footwear = new Footwear(intSize, name, dblPrice, intStock);
                            DBManager db = new DBManager();
                            db.addProduct(footwear);
                            
                            error_lbl.setText("New Footwear product added");
                    }
                }
                catch(Exception ex){
                    error_lbl.setText("Error: Please enter numbers only");
                }
                try{
                    if(clothing_rdb.isSelected()){
                            //String measurementIn, String productNameIn, double priceIn, int stockLevelIn
                            Clothing clothing = new Clothing(extra, name, dblPrice, intStock);
                            DBManager db = new DBManager();
                            db.addProduct(clothing);

                            error_lbl.setText("New Clothing product added");
                        }
                }
                catch(Exception ex){
                error_lbl.setText("Error: Please enter a single character only");
            }
            }
            catch(Exception ex){
                error_lbl.setText("Error: Please enter numbers only");
            }
        }
    }//GEN-LAST:event_add_btnActionPerformed

