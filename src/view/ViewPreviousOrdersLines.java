    public ViewPreviousOrdersLines(User loggedInUser, Customer cust, int orderId) {
        
        custWhoMadeOrder = cust;
        currentUser = loggedInUser;
        orderIdSelected = orderId;
        
        initComponents();
        
        DefaultTableModel orderLines = (DefaultTableModel)orderLines_tbl.getModel();
        
        Order orderSelected = custWhoMadeOrder.getOrders().get(orderId);
        
        for(Map.Entry<Integer, OrderLine> oEntry : orderSelected.getOrderLines().entrySet()){
            OrderLine actualOrderLine = oEntry.getValue();
            Product orderedProduct = actualOrderLine.getProduct();
            
            //if product deleted
            String productId = "";
            String product = "";
            String cost = "";
            
            if(orderedProduct.getProductId() == 0){
                productId = "N/A";
                product = "N/A";
                cost = "N/A";
            }
            else{
                productId = String.valueOf(orderedProduct.getProductId());
                product = orderedProduct.toString();
                cost = "Â£" + String.format("%.02f", orderedProduct.getPrice());
            }
            orderLines.addRow(new Object[]{productId, product, cost});
        }
    }

